def main():
    config = parse()
    if any(config.get(k) for k in ["-h", "--help"]):
        sys.exit(HELP)
    if config.get("--version") != None:
        sys.exit(version.__version__)
    pattern, usage, description = speclang.parse(config["spec_file"])
    if config.get("pattern"):
        print(tools.lst_to_pattern([pattern]))
        return
    leafs = embodiments.process(pattern)
    #print("#embodiments:", len(leafs))
    lutable = lut.generate(leafs)
    if not any(config.get(k) for k in ["-q", "--quiet"]):
        if not validate.is_ok(lutable):
            print("warning: CLI specification is ambiguous", file=sys.stderr)
    width = config.get("width")
    width = width if width == None else int(width)
    if config.get("lut"):
        spaghetti = templates.serialize(lutable, width)
        print('"%s"' % spaghetti)
    else:
        language = config.get("language", "python")
        rst = templates.export(lutable, usage, description, language, width)
        output_file = config.get("output_file", "cli.py")
        with open(output_file, "w") as f:
            f.write(rst)


if __name__ == "__main__":
    main()
